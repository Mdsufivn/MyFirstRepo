package Data_Types;

public class JavaOperators {

	public static void main(String[] args) {
	
	
		/** There are 5 types of Java operator
		 * 
		 * Arithmetic Operator
		 * Assignment Operator
		 * Comparission / Relational Operators
		 * Logical Operator
		 * Bitwise Operator
		 */
	
		//They are (+,-,*./,%,++,--)//
			
	int a = 10;
	int b = 5;
	int results;
	
	results = a + b; 
	System.out.println("Addition results is " + results); //15
	results = a - b; 
	System.out.println("Subtraction results is " + results); //5
	results = a * b; 
	System.out.println("Multiplication results is " + results); //50
	results = a / b; 
	System.out.println("Division results is " + results); //2
	results = a % b; 
	System.out.println("Modulus results is " + results); //0
	//System.out.println("Incremental results is " + a++); // 10
	//System.out.println("Decremental results is " + a--); // 11
	
	}
	
}
	