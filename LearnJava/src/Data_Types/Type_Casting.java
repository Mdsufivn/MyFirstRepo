package Data_Types;

public class Type_Casting {

	public static void main(String[] args) {
		// Type casting is the process of converting
		// one data types into another data types
		// Type casting is when you assign a value of one primitive data type to another data type.

			double myDouble = 56.98;
			System.out.println("Double value is : " + myDouble ); // Print double value
			// type casting inot in value
			int myInt = (int)myDouble;
			System.out.println("Double value is : " + myInt);// ?
	}
	

}
